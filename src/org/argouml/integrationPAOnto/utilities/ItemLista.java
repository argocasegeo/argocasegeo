package org.argouml.integrationPAOnto.utilities;

public class ItemLista {
	
	private String nome;
	private int espaco;
	
	public ItemLista(String nome, int espaco){
		this.nome = nome;
		this.espaco = espaco;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getEspaco() {
		return espaco;
	}

	public void setEspaco(int espaco) {
		this.espaco = espaco;
	}
	
	
	
}//fim do ItemLista
